export const SEVEN_IRON_RULES = `
## 七鐵律（每篇必須全部通過）

1. **開場永遠用「我」不用「你」**
   - ❌ "你應該…" → ✅ "我發現…"
   - 第一句建立作者視角，不是說教

2. **數字要具體到不像編的**
   - ❌ "很多人" → ✅ "143 個"
   - ❌ "月收差很多" → ✅ "月收差 4 倍"
   - 具體 = 可信

3. **每篇要有一句可截圖分享的金句**
   - 這句話獨立出來也能成立
   - 讀者截圖分享 = 免費擴散

4. **結尾用反問，不用總結**
   - ❌ "所以記住…" → ✅ "你呢？" "這樣想，會怎樣？"
   - 打開對話，不是關閉對話

5. **一行一句，不堆長段落**
   - 每 2-3 行空一行
   - 手機閱讀的呼吸感 = 停留時間

6. **括號 OS 增加人味**
   - "(好吧也有可能是我想太多)"
   - 括號裡放真實的內心碎念

7. **不說「你應該」，說「我發現」**
   - 透過觀察建立權威，不是透過指令
   - "我發現…" > "你應該…"
`.trim();

// ══════════════════════════════════════════════
// 禁止詞（Red flag — 出現就重寫）
// ══════════════════════════════════════════════

export const BANNED_WORDS = [
  '記住！', '請記住',
  '你應該', '你需要',
  '一起撐', '一起加油',
  '不是…而是',
  '溫柔地', '溫暖地',
  '值得被愛', '你很好',
  '按讚', '追蹤', '分享',  // 互動索取
] as const;

export const BANNED_WORDS_PROMPT = `
## 絕對禁止詞（出現 = 重寫）
${BANNED_WORDS.map(w => `- 「${w}」`).join('\n')}

替代原則：
- 「記住！」→ 直接陳述
- 「你應該/你需要」→ 「我發現」「有個說法是」
- 「一起撐/加油」→ 不說
- 「不是…而是」→ 直接說「是」的部分
- 「溫柔地」→ 用動作代替形容
- 「值得被愛/你很好」→ 用具體場景代替
`.trim();

